    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        avl = new Avaliacao();
        avl.setNotaAval(txtNota.getText());
        avl.setComentAval(txtComent.getText());
        
        avl.setJogo((Jogo) cmbJogo.getSelectedItem());
        avl.setUsuario((Usuario) cmbUsuario.getSelectedItem());
        
        if (avl.getJogo() == null) {
            JOptionPane.showMessageDialog(this, "Selecione um jogo!");
            return;
        }

        if (avl.getUsuario() == null) {
            JOptionPane.showMessageDialog(this, "Selecione um usuário!");
            return;
        }
        
        // Invocar o método create da classe DAO
        AvaliacaoDAO dao = new AvaliacaoDAO();
        dao.create(avl);
        
        // Limpar os campos e preencher novamente a tabela
        limpar();
        preencherTabela();
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        avl.setNotaAval(txtNota.getText());
        avl.setComentAval(txtComent.getText());
 
        avl.setJogo((Jogo) cmbJogo.getSelectedItem());
        avl.setUsuario((Usuario) cmbUsuario.getSelectedItem());
            
        AvaliacaoDAO dao = new AvaliacaoDAO();
        dao.update(avl);
        
        limpar();
        preencherTabela();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        if (avl != null) {
            if (JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir esta Avaliação?") == JOptionPane.YES_OPTION) {
                AvaliacaoDAO dao = new AvaliacaoDAO();
                dao.destroy(avl);

                limpar();
                preencherTabela();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed
    }
    private void tblAvaliacoesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblAvaliacoesMouseClicked
        // TODO add your handling code here:
        int linha = tblAvaliacoes.getSelectedRow();
        
        
        if(linha != -1) {
            String id = tblAvaliacoes.getValueAt(linha, 0).toString();
            AvaliacaoDAO dao = new AvaliacaoDAO();
            
            avl = dao.read(Integer.parseInt(id));
            
            // Preencher os campos do formulário
            txtNota.setText(avl.getNotaAval());
            txtComent.setText(avl.getComentAval());
            cmbJogo.setSelectedItem(avl.getJogo());
            cmbUsuario.setSelectedItem(avl.getUsuario());
        }
            
    }//GEN-LAST:event_tblAvaliacoesMouseClicked

