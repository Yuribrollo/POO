    public void preencherTabela() {
        DefaultTableModel dtm = (DefaultTableModel) tblAvaliacoes.getModel();
        dtm.setRowCount(0);
        AvaliacaoDAO dao = new AvaliacaoDAO();
        
        for(Avaliacao avl : dao.read()) {
            dtm.addRow(new Object[] {
                avl.getIdAval(),
                avl.getNotaAval(),
                avl.getComentAval(),
                avl.getJogo().getNomeJogo(),
                avl.getUsuario().getNomeUsu()
            });
        }
    }
    
    public void preencherComboJogo(){
        JogoDAO dao = new JogoDAO();
        
        for(Jogo jg : dao.read()) {
            cmbJogo.addItem(jg);
        }
    }
    
        public void preencherComboUsuario(){
        UsuarioDAO dao = new UsuarioDAO();
        
        for(Usuario usu : dao.read()) {
            cmbUsuario.addItem(usu);
        }
    }
    
