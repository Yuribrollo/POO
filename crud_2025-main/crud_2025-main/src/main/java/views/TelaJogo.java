    private void tblJogosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblJogosMouseClicked
        // TODO add your handling code here:
        int linha = tblJogos.getSelectedRow();
            String id = tblJogos.getValueAt(linha, 0).toString();

            if(linha != -1) {
                JogoDAO dao = new JogoDAO();
                jg = dao.read(Integer.parseInt(id));

                txtNomeJg.setText(jg.getNomeJogo());
                txtGenJg.setText(jg.getGeneroJogo());
                txtDescJg.setText(jg.getDescJogo());

            }
    }//GEN-LAST:event_tblJogosMouseClicked

