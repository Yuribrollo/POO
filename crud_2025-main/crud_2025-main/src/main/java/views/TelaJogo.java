    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        jg.setNomeJogo(txtNomeJg.getText());
        jg.setGeneroJogo(txtGenJg.getText());
        jg.setDescJogo(txtDescJg.getText());
        jg.setEquipe((Equipe) cmbEquipe.getSelectedItem());
        jg.setGamejam((GameJam) cmbGJ.getSelectedItem());
        
        JogoDAO dao = new JogoDAO();
        dao.update(jg);
        
        limpar();
        preencherTabela();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
                if (jg != null) {
            if (JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir este Jogo?") == JOptionPane.YES_OPTION) {
                JogoDAO dao = new JogoDAO();
                dao.destroy(jg);

                limpar();
                preencherTabela();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        jg = new Jogo();
        jg.setNomeJogo(txtNomeJg.getText());
        jg.setGeneroJogo(txtGenJg.getText());
        jg.setDescJogo(txtDescJg.getText());
        
        jg.setEquipe((Equipe) cmbEquipe.getSelectedItem());
        jg.setGamejam((GameJam) cmbGJ.getSelectedItem());
        
        if (jg.getEquipe() == null) {
            JOptionPane.showMessageDialog(this, "Selecione uma equipe!");
            return;
        }

        if (jg.getGamejam() == null) {
            JOptionPane.showMessageDialog(this, "Selecione um gamejam!");
            return;
        }
        
        // Invocar o método create da classe DAO
        JogoDAO dao = new JogoDAO();
        dao.create(jg);
        
        // Limpar os campos e preencher novamente a tabela
        limpar();
        preencherTabela();
    }//GEN-LAST:event_jButton2ActionPerformed

