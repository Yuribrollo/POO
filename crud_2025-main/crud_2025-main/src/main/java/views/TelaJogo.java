    public void limpar() {
        txtNomeJg.setText("");
        txtGenJg.setText("");
        txtDescJg.setText("");

    }
    
    public void preencherTabela() {
        DefaultTableModel dtm = (DefaultTableModel) tblJogos.getModel();
        dtm.setRowCount(0);
        JogoDAO dao = new JogoDAO();
        
        for(Jogo jg : dao.read()) {
            dtm.addRow(new Object[] {
                jg.getIdJogo(),
                jg.getNomeJogo(),
                jg.getGeneroJogo(),
                jg.getDescJogo(),
                jg.getEquipe().getNomeEquipe(),
                jg.getGamejam().getNomeGJ()
                   
            });
        }
    }
    
    public void preencherComboEquipe(){
       EquipeDAO dao = new EquipeDAO();
        
        for(Equipe eqp : dao.read()) {
            cmbEquipe.addItem(eqp);
        }
    }
    
        public void preencherComboGameJam(){
        GameJamDAO dao = new GameJamDAO();
        
        for(GameJam gj : dao.read()) {
            cmbGJ.addItem(gj);
        }
    }
    
    
