    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        eqp = new Equipe();
        eqp.setNomeEquipe(txtNomeEqp.getText().trim());

        List<Usuario> selecionados = listUsu.getSelectedValuesList();
        if (!selecionados.isEmpty()) {
            eqp.getUsuarios().addAll(selecionados);
        }

        // Salvar no banco (Equipe + usuários)
        EquipeDAO dao = new EquipeDAO();
        dao.create(eqp);

        // Limpar campos e atualizar tabela
        limpar();
        preencherTabela();

        // Limpar seleção da JList
        listUsu.clearSelection();
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        eqp.setNomeEquipe(txtNomeEqp.getText());


        List<Usuario> selecionados = listUsu.getSelectedValuesList();
        eqp.getUsuarios().clear();         
        eqp.getUsuarios().addAll(selecionados); 

       
        EquipeDAO dao = new EquipeDAO();
        dao.update(eqp);

// Limpar campos e atualizar tabela
limpar();
preencherTabela();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        if (eqp != null) {
            if (JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir esta Equipe?") == JOptionPane.YES_OPTION) {
                EquipeDAO dao = new EquipeDAO();
                dao.destroy(eqp);

                limpar();
                preencherTabela();
        }
    }
    }//GEN-LAST:event_btnExcluirActionPerformed

