    private void tblEquipesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEquipesMouseClicked
        // TODO add your handling code here:
            int linha = tblEquipes.getSelectedRow();
            String id = tblEquipes.getValueAt(linha, 0).toString();

            if(linha != -1) {
                EquipeDAO dao = new EquipeDAO();
                eqp = dao.read(Integer.parseInt(id));

                txtNomeEqp.setText(eqp.getNomeEquipe());

            }
    }//GEN-LAST:event_tblEquipesMouseClicked

    public void limpar() {
        txtNomeEqp.setText("");
        
    }
    
    public void preencherTabela() {
        DefaultTableModel dtm = (DefaultTableModel) tblEquipes.getModel();
        dtm.setRowCount(0);
        EquipeDAO dao = new EquipeDAO();
        
        for (String[] row : dao.readWithUsuarios()) {
        dtm.addRow(new Object[] {
            row[0], // ID
            row[1], // Nome da equipe
            row[2]  // Participantes concatenados
        });
        }
    }

